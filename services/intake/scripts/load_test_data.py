def parse_args() -> argparse.Namespace:
    """Parse command line arguments."""
    parser = argparse.ArgumentParser(
        description="Load test entry data into Intake API",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  %(prog)s                                    # Load all data to localhost
  %(prog)s --dry-run                          # Validate without posting
  %(prog)s --url http://api.example.com:8000  # Load to custom API
  %(prog)s --categories single_turns          # Load only single-turn entries
  %(prog)s --data-dir /path/to/data           # Use custom data directory
  %(prog)s --workers 20                       # Use 20 concurrent workers
        """,
    )

    parser.add_argument(
        "--url",
        default="http://localhost:8080",
        help="Base URL of the NeMo Platform gateway (default: http://localhost:8080)",
    )

    parser.add_argument(
        "--workspace",
        default="default",
        help="Workspace to write entries into (default: default)",
    )

    parser.add_argument(
        "--data-dir",
        type=Path,
        default=Path(__file__).resolve().parents[3] / ".tmp" / "intake" / "test_data",
        help="Directory containing JSON data files (default: <repo>/.tmp/intake/test_data)",
    )

    parser.add_argument(
        "--categories",
        nargs="*",
        choices=["single_turns", "conversations", "feedback_heavy", "action_heavy"],
        help="Specific categories to load (default: all)",
    )

    parser.add_argument(
        "--workers",
        type=int,
        default=3,
        help="Number of concurrent workers (default: 3). Higher values can overwhelm a local intake instance.",
    )

    parser.add_argument("--dry-run", action="store_true", help="Validate data without sending requests")

    return parser.parse_args()


# ---------------------------------------------------------------------------
# Main Function
# ---------------------------------------------------------------------------


def main() -> int:
    """Main loading function."""
    args = parse_args()

    print("🎯 Intake API Data Loader")
    print("=" * 50)
    print(f"API URL: {args.url}")
    print(f"Workspace: {args.workspace}")
    print(f"Data directory: {args.data_dir}")
    print(f"Categories: {args.categories or 'all'}")
    print(f"Workers: {args.workers}")
    print(f"Dry run: {args.dry_run}")
    print()

    try:
        post_url = entries_url(args.url, args.workspace)

        # Verify API connectivity (unless dry run)
        if not args.dry_run:
            if not verify_api_connectivity(post_url):
                return 1

        # Load entry data
        dataset = load_entry_data(args.data_dir, args.categories)
        if not dataset:
            print("❌ No data to load")
            return 1

        # Create HTTP session with a connection pool sized for the worker count
        session = create_http_session(pool_size=max(1, args.workers))

        progress = load_dataset_parallel(
            session=session, post_url=post_url, dataset=dataset, dry_run=args.dry_run, max_workers=args.workers
        )

        # Return appropriate exit code
        if progress.failed == 0:
            print(f"\n✅ {'Validation' if args.dry_run else 'Loading'} completed successfully!")
            return 0
        elif progress.successful > 0:
            print(f"\n⚠️  {'Validation' if args.dry_run else 'Loading'} completed with {progress.failed} errors")
            return 1
        else:
            print(f"\n❌ {'Validation' if args.dry_run else 'Loading'} failed completely")
            return 2

    except KeyboardInterrupt:
        print("\n\n⏹️  Loading interrupted by user")
        return 130
    except Exception as e:
        print(f"\n💥 Unexpected error: {e}")
        return 1


if __name__ == "__main__":
    sys.exit(main())
