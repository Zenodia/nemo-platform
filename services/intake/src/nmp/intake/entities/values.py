class UserActionEvent(_BaseEvent):
    """Free-form user action captured by the client application.

    Use this to track arbitrary user interactions with AI responses, such as copying code,
    clicking share buttons, making purchases, or any other measurable action.

    The action identifier must be ≤256 characters and should use snake-case or kebab-case
    for consistency (e.g., ``share_clicked``, ``code_copied``, ``purchase_made``).
    """

    model_config = ConfigDict(extra="forbid")

    event_type: Literal["user_action"] = "user_action"  # noqa: A003

    action: str = Field(
        ...,
        max_length=256,
        description=(
            "Descriptive name for the action taken by the user (e.g., 'share_clicked', 'code_copied', 'link_followed'). "
            "Use snake-case or kebab-case. This is a label, not a unique identifier—multiple events can have the same action name."
        ),
    )
    metadata: Dict[ShortStr, UserActionValue] = Field(
        default_factory=dict,
        description=(
            "Optional key-value pairs with additional context about the action (max 16 entries). "
            "Use this for details like user IDs, item IDs, timestamps, A/B test variants, or any other "
            "information useful for downstream training or evaluation pipelines. "
            "Example: {'user_id': '12345', 'experiment': 'variant_b', 'item_purchased': 'product_456'}."
        ),
        max_length=16,
    )


class ReviewerAnnotationEvent(UserFeedbackEvent):
    """Structured annotation supplied by a reviewer or expert evaluator.

    A reviewer annotation is similar to user feedback but includes an additional capability
    to provide a complete replacement response. This is useful when human experts need to
    correct not just the text but also structured elements like tool calls, function outputs,
    or other response metadata.

    Inherits all feedback fields from UserFeedbackEvent (thumb, rating, opinion, rewrite,
    chosen_index, categories) and adds response_override for full response replacement.
    """

    event_type: Literal["reviewer_annotation"] = "reviewer_annotation"  # type: ignore[assignment]

    response_override: Optional[Dict[str, Any]] = Field(
        default=None,
        description=(
            "Complete JSON object that replaces the original model response when exporting data. "
            "Unlike the `rewrite` field (which is just text), this can include tool calls, function outputs, "
            "and all other response metadata. When an entry with response_override is exported, you can choose "
            "to use this corrected response instead of the original. "
            "Example: {'choices': [{'message': {'role': 'assistant', 'content': 'Corrected text', "
            "'tool_calls': [...]}}]}"
        ),
    )


class EvaluatorResultEvent(_BaseEvent):
    """Result produced by an automated evaluator.

    Use this for any non-human scorer that emits a judgement about an entry:
    eval-framework verifier rewards, LLM-judge ratings, auditor probe results, etc.
    Distinct from UserFeedbackEvent (human end-user feedback) and ReviewerAnnotationEvent
    (human expert annotation).
    """

    model_config = ConfigDict(extra="forbid")

    event_type: Literal["evaluator_result"] = "evaluator_result"  # noqa: A003

    name: str = Field(
        ...,
        max_length=256,
        description=(
            "Identifier of the evaluator that produced this result "
            "(e.g., 'harbor.verifier', 'evaluator.llm_judge', 'auditor.pii_probe')."
        ),
    )
    score: Optional[Union[int, float, str]] = Field(
        default=None,
        description=(
            "The result value: a number (e.g., reward, rating, probability) or a string "
            "(e.g., 'pass', 'fail', a category label). Semantics are defined by the evaluator."
        ),
    )
    metadata: Optional[Dict[str, Any]] = Field(
        default=None,
        description=(
            "Free-form additional context about this result "
            "(e.g., supporting metrics, trial_name, rubric_version, evaluator config snapshot)."
        ),
    )


# Discriminated union --------------------------------------------------------

EntryEvent = Annotated[
    Union[UserFeedbackEvent, UserActionEvent, ReviewerAnnotationEvent, EvaluatorResultEvent],
    Field(discriminator="event_type"),
]


