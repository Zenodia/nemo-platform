# Data Loading Functions
# ---------------------------------------------------------------------------


def load_entry_data(data_dir: Path, categories: Optional[List[str]] = None) -> Dict[str, List[Dict[str, Any]]]:
    """
    Load entry data from JSON files.

    Args:
        data_dir: Directory containing JSON files
        categories: Optional list of specific categories to load

    Returns:
        Dictionary mapping category names to entry lists
    """
    if not data_dir.exists():
        raise FileNotFoundError(f"Data directory not found: {data_dir}")

    available_files = {
        "single_turns": data_dir / "single_turns.json",
        "conversations": data_dir / "conversations.json",
        "feedback_heavy": data_dir / "feedback_heavy.json",
        "action_heavy": data_dir / "action_heavy.json",
        "all_entries": data_dir / "all_entries.json",
    }

    # Filter to requested categories
    if categories:
        files_to_load = {cat: available_files[cat] for cat in categories if cat in available_files}
        missing = set(categories) - set(available_files.keys())
        if missing:
            print(f"⚠️  Unknown categories: {missing}")
    else:
        # Load all except the combined file
        files_to_load = {k: v for k, v in available_files.items() if k != "all_entries"}

    dataset = {}
    total_entries = 0

    for category, file_path in files_to_load.items():
        if not file_path.exists():
            print(f"⚠️  File not found: {file_path}")
            continue

        try:
            with open(file_path, "r") as f:
                entries = json.load(f)

            dataset[category] = entries
            total_entries += len(entries)
            print(f"📁 Loaded {len(entries)} entries from {category}")

        except (json.JSONDecodeError, IOError) as e:
            print(f"❌ Error loading {file_path}: {e}")

    print(f"📊 Total entries loaded: {total_entries}")
    return dataset


def post_entry(
    session: requests.Session,
    post_url: str,
    entry: Dict[str, Any],
    dry_run: bool = False,
    timeout: float = 30.0,
) -> Tuple[bool, Optional[str]]:
    """
    POST a single entry to the v2 entries endpoint.

    Args:
        session: HTTP session
        post_url: Full URL of the v2 entries endpoint
        entry: Entry data to post
        dry_run: If True, validate but don't send request
        timeout: Per-request timeout in seconds

    Returns:
        Tuple of (success, error_message)
    """
    if dry_run:
        required_fields = ["data", "context"]
        missing = [field for field in required_fields if field not in entry]
        if missing:
            return False, f"Missing required fields: {missing}"

        request = entry["data"].get("request")
        response = entry["data"].get("response")
        context = entry.get("context")

        if not isinstance(request, dict) or not request.get("model") or not request.get("messages"):
            return False, "Invalid data.request; expected non-empty model and messages"
        if not isinstance(response, dict) or not isinstance(response.get("choices"), list) or not response["choices"]:
            return False, "Invalid data.response; expected non-empty choices"
        first_choice = response["choices"][0]
        message = first_choice.get("message") if isinstance(first_choice, dict) else None
        if not isinstance(message, dict) or "role" not in message:
            return False, "Invalid data.response.choices[0].message"
        if not isinstance(context, dict) or "app" not in context or "task" not in context:
            return False, "Invalid context; expected app and task"
        return True, None

    try:
        response = session.post(
            post_url,
            json=entry,
            headers={"Content-Type": "application/json"},
            timeout=timeout,
        )

        if response.status_code in [200, 201]:
            return True, None
        else:
            return False, f"HTTP {response.status_code}: {response.text[:200]}"

    except requests.RequestException as e:
        return False, f"Request failed: {str(e)[:100]}"


