# Parallel Loading with Progress Tracking
# ---------------------------------------------------------------------------


class LoadingProgress:
    """Track loading progress and statistics."""

    def __init__(self, total_count: int):
        self.total_count = total_count
        self.completed = 0
        self.successful = 0
        self.failed = 0
        self.start_time = time.time()
        self.errors: List[str] = []

    def update(self, success: bool, error: Optional[str] = None) -> None:
        """Update progress counters."""
        self.completed += 1
        if success:
            self.successful += 1
        else:
            self.failed += 1
            if error:
                self.errors.append(error)

    def print_progress(self) -> None:
        """Print current progress to console."""
        elapsed = time.time() - self.start_time
        rate = self.completed / elapsed if elapsed > 0 else 0

        progress_pct = (self.completed / self.total_count) * 100

        print(
            f"\r📈 Progress: {self.completed}/{self.total_count} ({progress_pct:.1f}%) | "
            f"✅ {self.successful} | ❌ {self.failed} | {rate:.1f}/sec",
            end="",
            flush=True,
        )

    def print_summary(self) -> None:
        """Print final loading summary."""
        elapsed = time.time() - self.start_time
        print("\n\n📊 Loading Summary:")
        print(f"   • Total entries: {self.total_count}")
        print(f"   • Successful: {self.successful}")
        print(f"   • Failed: {self.failed}")
        print(f"   • Success rate: {(self.successful / self.total_count) * 100:.1f}%")
        print(f"   • Total time: {elapsed:.1f}s")
        print(f"   • Average rate: {self.total_count / elapsed:.1f} entries/sec")

        if self.errors:
            print("\n❌ Error samples:")
            for error in self.errors[:5]:  # Show first 5 errors
                print(f"   • {error}")
            if len(self.errors) > 5:
                print(f"   • ... and {len(self.errors) - 5} more errors")


def load_dataset_parallel(
    session: requests.Session,
    post_url: str,
    dataset: Dict[str, List[Dict[str, Any]]],
    dry_run: bool = False,
    max_workers: int = 10,
) -> LoadingProgress:
    """
    Load all entries with progress tracking, optionally in parallel.

    Args:
        session: HTTP session (requests.Session is thread-safe for send())
        post_url: Full URL of the v2 entries endpoint
        dataset: Dictionary of categorized entry data
        dry_run: If True, validate but don't send requests
        max_workers: Maximum concurrent requests. 1 runs sequentially.

    Returns:
        LoadingProgress object with final statistics
    """
    all_entries = [entry for entries in dataset.values() for entry in entries]
    if not all_entries:
        raise ValueError("No entries found in the selected dataset")

    progress = LoadingProgress(len(all_entries))

    mode = "dry-run validation" if dry_run else "data loading"
    effective_workers = 1 if dry_run else max(1, max_workers)
    print(f"🚀 Starting {mode} with {effective_workers} worker(s)...")

    def record(success: bool, err: Optional[str]) -> None:
        progress.update(success, err)
        if progress.completed % 10 == 0 or progress.completed == progress.total_count:
            progress.print_progress()

    if effective_workers == 1:
        for entry in all_entries:
            record(*post_entry(session, post_url, entry, dry_run))
    else:
        with ThreadPoolExecutor(max_workers=effective_workers) as executor:
            futures = [executor.submit(post_entry, session, post_url, entry, dry_run) for entry in all_entries]
            for future in as_completed(futures):
                record(*future.result())

    progress.print_summary()

    return progress


