class Usage(Value):
    """Structured usage metrics captured at log time.

    Every field is optional so producers can populate whatever they have without
    schema breakage. Stored as the entry-level ``usage`` field so filters can
    reach it via ``data.usage.<field>`` entity-store paths.
    """

    model: Optional[str] = Field(
        default=None,
        description="The actual model that served the request (after any routing). May differ from the model in the request body.",
    )
    started_at: Optional[datetime] = Field(
        default=None,
        description="UTC timestamp when the upstream LLM call started.",
        json_schema_extra={"format": "date-time"},
    )
    ended_at: Optional[datetime] = Field(
        default=None,
        description="UTC timestamp when the upstream LLM call ended.",
        json_schema_extra={"format": "date-time"},
    )
    latency_ms: Optional[int] = Field(
        default=None,
        ge=0,
        description="Wall-clock latency of the upstream LLM call, in milliseconds.",
        json_schema_extra={"minimum": 0},
    )
    cost_usd: Optional[float] = Field(
        default=None,
        ge=0,
        description="Total estimated cost of this call, in USD.",
        json_schema_extra={"minimum": 0},
    )
    cost_input_usd: Optional[float] = Field(
        default=None,
        ge=0,
        description="Estimated cost attributed to input tokens, in USD.",
        json_schema_extra={"minimum": 0},
    )
    cost_output_usd: Optional[float] = Field(
        default=None,
        ge=0,
        description="Estimated cost attributed to output tokens, in USD.",
        json_schema_extra={"minimum": 0},
    )
    input_tokens: Optional[int] = Field(
        default=None,
        ge=0,
        description="Number of input tokens consumed.",
        json_schema_extra={"minimum": 0},
    )
    output_tokens: Optional[int] = Field(
        default=None,
        ge=0,
        description="Number of output tokens produced.",
        json_schema_extra={"minimum": 0},
    )
    cached_tokens: Optional[int] = Field(
        default=None,
        ge=0,
        description="Number of input tokens served from a prompt cache (subset of input_tokens).",
        json_schema_extra={"minimum": 0},
    )

    @field_validator("started_at", "ended_at")
    @classmethod
    def _ensure_utc_timestamp(cls, value: Optional[datetime]) -> Optional[datetime]:
        if value is None:
            return value
        if value.tzinfo is None or value.utcoffset() is None:
            return value.replace(tzinfo=timezone.utc)
        return value.astimezone(timezone.utc)

    @model_validator(mode="after")
    def _validate_timestamp_order(self) -> Self:
        if self.started_at is not None and self.ended_at is not None and self.ended_at < self.started_at:
            raise ValueError("ended_at must be greater than or equal to started_at")
        return self

    @model_validator(mode="after")
    def _validate_cached_tokens(self) -> Self:
        if self.cached_tokens is not None and self.input_tokens is not None and self.cached_tokens > self.input_tokens:
            raise ValueError("cached_tokens must be less than or equal to input_tokens")
        return self


class EntryData(Value):
    """Entry data containing the request and response for an LLM interaction."""

    request: FlexibleEntryRequest = Field(..., description="Raw request payload recorded from the client.")
    response: FlexibleEntryResponse = Field(..., description="Raw response payload generated by the model.")


class EntryContext(Value):
    """Contextual metadata attached to every entry record.

    Keeping these grouped in a dedicated object avoids polluting the top-level
    entity schema and makes it trivial to extend without breaking compatibility.
    """

    # Required fields ------------------------------------------------------

    app: str = Field(
        ...,
        description=(
            "Reference to the app that produced this entry, in the form `workspace/name`. "
            "If the app doesn't exist, it will be automatically created when the entry is ingested."
        ),
    )
    task: Optional[str] = Field(
        ...,
        description=(
            "Name of the task within the app (e.g., 'chat', 'completion', 'tool-call'). "
            "If the task doesn't exist, it will be automatically created when the entry is ingested."
        ),
    )

    thread_id: Optional[str] = Field(
        default=None,
        description=(
            "Logical thread identifier that groups related entries in a multi-turn conversation. "
            "If provided, entries with the same thread_id are treated as part of the same conversation. "
            "If omitted, the entry is treated as a single-turn interaction (e.g., a standalone tool call or completion)."
        ),
    )

    user_id: Optional[str] = Field(
        default=None,
        description=(
            "Identifier of the application's end-user who triggered this LLM interaction. "
            "This represents the person using your application (e.g., 'customer_123', 'employee@company.com'), "
            "NOT the service account that created the entry record (see ownership.created_by for that). "
            "Use this to track which of your users a conversation belongs to, filter entries by user, "
            "and enable per-user analytics. Format is application-defined."
        ),
    )

    # Optional tracing fields -----------------------------------------------
    trace_id: Optional[str] = Field(
        default=None,
        description=(
            "Distributed trace identifier (e.g., W3C traceparent). Intake stores it "
            "verbatim and does not use it at ingestion time; helps with later cross-system joins."
        ),
    )

    session_id: Optional[str] = Field(
        default=None,
        description=(
            "Long-lived session identifier (e.g., user account or browser session). "
            "Stored for post-processing analytics; not used by the Intake service at runtime."
        ),
    )

    # Timestamps
    created_at: datetime = Field(
        default_factory=lambda: datetime.now(timezone.utc),
        description="UTC timestamp when the entry was created.",
    )


class UserRating(Value):
    """User's rating/evaluation of an AI response.

    This captures various forms of end-user feedback about a model's response, including
    binary thumbs up/down ratings, numeric scores, free-text opinions, suggested rewrites,
    and structured category ratings.

    Either `thumb` or `rating` should be provided (they are mutually exclusive), but all
    fields are optional to accommodate different feedback collection patterns.
    """

    thumb: Optional[ThumbDirection] = Field(
        default=None,
        description=(
            'Binary feedback: "up" for 👍 or "down" for 👎. '
            "Mutually exclusive with `rating`. Use this for simple thumbs up/down UI elements."
        ),
    )
    rating: Optional[float] = Field(
        default=None,
        ge=0,
        description=(
            "Numeric rating (e.g., 1-5 stars) provided by the end user. "
            "Mutually exclusive with `thumb`. Use this for star ratings or numeric scales."
        ),
    )
    opinion: Optional[str] = Field(
        default=None,
        min_length=1,
        max_length=2_000,
        description="Free-text comment from the end user describing their opinion of the response.",
    )
    rewrite: Optional[str] = Field(
        default=None,
        min_length=1,
        max_length=10_000,
        description=(
            "End-user's suggested text replacement for the generated response. "
            "This is the user's idea of what the response should have been."
        ),
    )
    chosen_index: Optional[int] = Field(
        default=None,
        ge=0,
        description=(
            "Zero-based index of the response option the user selected when multiple responses were returned. "
            "Use this when showing users multiple completion choices and tracking which one they picked."
        ),
    )
    categories: Optional[Dict[str, Union[float, str]]] = Field(
        default=None,
        description=(
            "Application-specific category ratings as key-value pairs. "
            "Use this for custom rating dimensions (e.g., {'helpfulness': 4, 'accuracy': 5, 'tone': 'professional'}). "
            "Useful for radio buttons, dropdowns, or multi-dimensional rating systems."
        ),
    )

    @field_validator("rewrite")
    @classmethod
    def _strip_whitespace(cls, v: Optional[str]):  # noqa: D401, N805
        return v.strip() if isinstance(v, str) else v


