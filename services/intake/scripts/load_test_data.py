"""Load generated test dataset into Intake API via HTTP requests.

This script takes the JSON files generated by generate_test_dataset.py and
POSTs them to the Intake API /entries endpoint.

Features:
- Parallel loading with configurable concurrency
- Progress tracking with real-time statistics
- Comprehensive error handling and reporting
- Dry-run mode for validation
- Flexible data directory and URL configuration
- Optional category filtering

Usage:
    python load_test_data.py                    # Load all data to localhost:8080
    python load_test_data.py --dry-run          # Validate without posting
    python load_test_data.py --url https://nemo.example.com --workspace my-team
    python load_test_data.py --categories single_turns conversations  # Load specific categories
"""

from __future__ import annotations

import argparse
import json
import sys
import time
from concurrent.futures import ThreadPoolExecutor, as_completed
from pathlib import Path
from typing import Any, Dict, List, Optional, Tuple

import requests
from requests.adapters import HTTPAdapter

# ---------------------------------------------------------------------------
# HTTP Client Configuration
# ---------------------------------------------------------------------------


def create_http_session(pool_size: int = 10) -> requests.Session:
    """Create HTTP session with retry strategy and a connection pool large enough for concurrent workers."""
    session = requests.Session()

    adapter = HTTPAdapter(max_retries=3, pool_connections=pool_size, pool_maxsize=pool_size)
    session.mount("http://", adapter)
    session.mount("https://", adapter)

    return session


def entries_url(base_url: str, workspace: str) -> str:
    """Build the v2 entries endpoint URL."""
    return f"{base_url.rstrip('/')}/apis/intake/v2/workspaces/{workspace}/entries"


def verify_api_connectivity(post_url: str) -> bool:
    """Verify the entries endpoint is reachable by listing one entry."""
    try:
        response = requests.get(post_url, params={"page": 1, "page_size": 1}, timeout=5)
        if response.status_code == 200:
            print(f"✅ Entries endpoint reachable: {post_url}")
            return True
        print(f"❌ Entries endpoint returned {response.status_code}: {response.text[:200]}")
        return False
    except requests.RequestException as e:
        print(f"❌ Cannot reach entries endpoint at {post_url}: {e}")
        return False


