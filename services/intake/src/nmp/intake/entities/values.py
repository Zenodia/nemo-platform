class FlexibleMessage(BaseModel):
    """A flexible message model that requires a valid role field but allows any other fields.

    This flexibility enables the Intake service to store messages from various LLM providers
    and future model types without requiring schema updates. Additional fields like `content`,
    `name`, `tool_calls`, `tool_call_id`, etc. are all accepted.

    Examples of additional fields:
    - `content`: The message text or content
    - `name`: Name of the user or function
    - `tool_calls`: Tool/function calls in the message
    - `tool_call_id`: ID of the tool call being responded to
    """

    model_config = ConfigDict(extra="allow")

    role: MessageRole = Field(
        ...,
        description=(
            "The role of the message sender. Must be one of: user, system, assistant, developer, tool, or function."
        ),
    )


class FlexibleEntryRequest(BaseModel):
    """Flexible entry request that accepts any object shape.

    This flexibility enables the Intake service to store requests from various LLM providers
    (OpenAI, Anthropic, NIM, etc.) and future model types (embeddings, multimodal, etc.)
    without requiring schema updates.

    Required fields: `messages` and `model`
    Common optional fields: `temperature`, `max_tokens`, `top_p`, `tools`, `tool_choice`,
    `stream`, `response_format`, etc.
    """

    model_config = ConfigDict(extra="allow")

    messages: List[FlexibleMessage] = Field(
        ...,
        description=(
            "A list of messages comprising the conversation. Each message must have a valid role. "
            "Additional fields like `content`, `tool_calls`, etc. are provider-specific."
        ),
    )
    model: str = Field(
        ...,
        description="The model identifier used for this request (e.g., 'gpt-4', 'llama-3-70b', 'claude-3-opus').",
    )


class FlexibleEntryResponse(BaseModel):
    """Flexible entry response that accepts any object shape.

    This flexibility enables the Intake service to store responses from various LLM providers
    and future model types without requiring schema updates.

    Required: either `choices` (successful response) or `error` (failed call).
    Common optional fields: `id`, `created`, `model`, `usage`, `system_fingerprint`, etc.
    """

    model_config = ConfigDict(
        extra="allow",
        json_schema_extra={
            "oneOf": [
                {"required": ["choices"]},
                {"required": ["error"]},
            ],
        },
    )

    choices: Optional[List[Dict[str, Any]]] = Field(
        default=None,
        description=(
            "A list of response choices generated by the model. Each choice typically contains "
            "a message object with role, content, and optional tool_calls. "
            "The structure varies by provider but commonly includes fields like `index`, `message`, and `finish_reason`."
        ),
    )
    error: Optional[Dict[str, Any]] = Field(
        default=None,
        description=(
            "Error envelope returned by the provider when the call failed (e.g., OpenAI's "
            "`{message, type, code, param}` shape). Mutually exclusive with `choices`: a response "
            "carries one or the other."
        ),
    )

    @model_validator(mode="after")
    def _require_choices_or_error(self) -> Self:
        # XOR by presence: choices=[] still counts as "present" — the entries endpoint
        # historically accepts callers that supply an empty list, so we treat omission
        # (None) as the only "missing" state.
        if (self.choices is None) == (self.error is None):
            if self.choices is None:
                raise ValueError("response must include either `choices` or `error`")
            raise ValueError("response cannot include both `choices` and `error`")
        return self


